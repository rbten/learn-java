public class PrimitiveVariable {
    public static void main(String[] args) {
        int x = 12;
        char initial = 'j';
        double d = 456.709;
        boolean isLearning;
        isLearning = true;
        int y = x + 456;

        System.out.println(x);
        System.out.println(initial);
        System.out.println(d);
        System.out.println(isLearning);
        System.out.println(y);
    }
}