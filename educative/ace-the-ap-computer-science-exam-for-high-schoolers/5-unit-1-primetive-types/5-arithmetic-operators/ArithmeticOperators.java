class ArithmeticOperators {
    public static void main(String[] args) {
        int var1 = 9;
        int var2 = 2;

        System.out.println(var1 + var2);
        System.out.println(var1 - var2);
        System.out.println(var1 * var2);
        System.out.println(var1 / var2);
        System.out.println(var1 % var2);
    }
}