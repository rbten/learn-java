class PrimitiveDataTypes {
    public static void main(String[] args) {
        int integerNumber;
        integerNumber = 7;

        System.out.println(integerNumber);

        double pi;
        double avogadroNumber;

        pi = 3.14159265359;
        System.out.println(pi);

        avogadroNumber =  6.02E23;
        System.out.println(avogadroNumber);


        boolean boolVariable;
        boolVariable = true;
        System.out.println(boolVariable);

        boolVariable = false;
        System.out.println(boolVariable);
    }   

}